package bekk;

public class OtherObject {
   private String someValue;
   private int number;

   public OtherObject() {
   }

   public OtherObject(String someValue, int number) {
      this.someValue = someValue;
      this.number = number;
   }

   public String getSomeValue() {
      return someValue;
   }

   public int getNumber() {
      return number;
   }
}
